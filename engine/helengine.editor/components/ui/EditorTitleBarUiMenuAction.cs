namespace helengine.editor {
    /// <summary>
    /// Enumerates built-in UI menu actions raised by the editor title bar.
    /// </summary>
    public enum EditorTitleBarUiMenuAction {
        /// <summary>
        /// Opens one new viewport panel instance.
        /// </summary>
        ShowViewport,
        /// <summary>
        /// Opens one new scene hierarchy panel instance.
        /// </summary>
        ShowSceneHierarchy,
        /// <summary>
        /// Opens one new asset browser panel instance.
        /// </summary>
        ShowAssetBrowser,
        /// <summary>
        /// Opens one new properties panel instance.
        /// </summary>
        ShowProperties,
        /// <summary>
        /// Opens one new logger panel instance.
        /// </summary>
        ShowLogger,
        /// <summary>
        /// Opens one new preview panel instance.
        /// </summary>
        ShowPreview,
        /// <summary>
        /// Saves the current workspace layout to slot 1.
        /// </summary>
        SaveSlot1,
        /// <summary>
        /// Saves the current workspace layout to slot 2.
        /// </summary>
        SaveSlot2,
        /// <summary>
        /// Saves the current workspace layout to slot 3.
        /// </summary>
        SaveSlot3,
        /// <summary>
        /// Saves the current workspace layout to slot 4.
        /// </summary>
        SaveSlot4,
        /// <summary>
        /// Saves the current workspace layout to slot 5.
        /// </summary>
        SaveSlot5,
        /// <summary>
        /// Loads the workspace layout from slot 1.
        /// </summary>
        LoadSlot1,
        /// <summary>
        /// Loads the workspace layout from slot 2.
        /// </summary>
        LoadSlot2,
        /// <summary>
        /// Loads the workspace layout from slot 3.
        /// </summary>
        LoadSlot3,
        /// <summary>
        /// Loads the workspace layout from slot 4.
        /// </summary>
        LoadSlot4,
        /// <summary>
        /// Loads the workspace layout from slot 5.
        /// </summary>
        LoadSlot5
    }
}
