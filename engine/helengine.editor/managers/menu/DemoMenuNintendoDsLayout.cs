namespace helengine.editor {
    /// <summary>
    /// Stores the authored layout constants used by the Nintendo DS demo-disc menu scene generator.
    /// </summary>
    public static class DemoMenuNintendoDsLayout {
        /// <summary>
        /// Width of one Nintendo DS screen in authored pixels.
        /// </summary>
        public const int ScreenWidth = 256;

        /// <summary>
        /// Height of one Nintendo DS screen in authored pixels.
        /// </summary>
        public const int ScreenHeight = 192;

        /// <summary>
        /// Width of the stacked dual-screen authored canvas.
        /// </summary>
        public const int StackedCanvasWidth = 256;

        /// <summary>
        /// Height of the stacked dual-screen authored canvas.
        /// </summary>
        public const int StackedCanvasHeight = 384;

        /// <summary>
        /// Width of the bottom-screen panel surface.
        /// </summary>
        public const int PanelWidth = 240;

        /// <summary>
        /// Height of the bottom-screen panel surface.
        /// </summary>
        public const int PanelHeight = 176;

        /// <summary>
        /// Width of one Nintendo DS menu button row.
        /// </summary>
        public const int ButtonWidth = 216;

        /// <summary>
        /// Height of one Nintendo DS menu button row.
        /// </summary>
        public const int ButtonHeight = 18;

        /// <summary>
        /// Vertical spacing inserted between adjacent Nintendo DS button rows.
        /// </summary>
        public const int ButtonSpacing = 4;

        /// <summary>
        /// Maximum logo width rendered on the top screen.
        /// </summary>
        public const int LogoMaxWidth = 144;

        /// <summary>
        /// Maximum logo height rendered on the top screen.
        /// </summary>
        public const int LogoMaxHeight = 96;
    }
}
