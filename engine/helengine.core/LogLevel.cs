namespace helengine {
    /// <summary>
    /// Defines the severity level for log messages.
    /// </summary>
    public enum LogLevel {
        /// <summary>
        /// Informational message.
        /// </summary>
        Info,
        /// <summary>
        /// Warning message that indicates a potential issue.
        /// </summary>
        Warning,
        /// <summary>
        /// Error message that indicates a failure.
        /// </summary>
        Error
    }
}
