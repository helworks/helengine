namespace helengine;

/// <summary>
/// Names the abstract buttons exposed by a gamepad-style controller.
/// </summary>
public enum InputGamepadButton {
    /// <summary>
    /// The south face button.
    /// </summary>
    South = 0,
    /// <summary>
    /// The east face button.
    /// </summary>
    East = 1,
    /// <summary>
    /// The west face button.
    /// </summary>
    West = 2,
    /// <summary>
    /// The north face button.
    /// </summary>
    North = 3,
    /// <summary>
    /// The left shoulder button.
    /// </summary>
    LeftShoulder = 4,
    /// <summary>
    /// The right shoulder button.
    /// </summary>
    RightShoulder = 5,
    /// <summary>
    /// The left stick button.
    /// </summary>
    LeftStick = 6,
    /// <summary>
    /// The right stick button.
    /// </summary>
    RightStick = 7,
    /// <summary>
    /// The left trigger digital edge.
    /// </summary>
    LeftTrigger = 8,
    /// <summary>
    /// The right trigger digital edge.
    /// </summary>
    RightTrigger = 9,
    /// <summary>
    /// The directional pad up button.
    /// </summary>
    DPadUp = 10,
    /// <summary>
    /// The directional pad down button.
    /// </summary>
    DPadDown = 11,
    /// <summary>
    /// The directional pad left button.
    /// </summary>
    DPadLeft = 12,
    /// <summary>
    /// The directional pad right button.
    /// </summary>
    DPadRight = 13,
    /// <summary>
    /// The start button.
    /// </summary>
    Start = 14,
    /// <summary>
    /// The select or back button.
    /// </summary>
    Select = 15,
    /// <summary>
    /// The platform or home button.
    /// </summary>
    Home = 16
}
