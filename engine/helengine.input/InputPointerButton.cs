namespace helengine;

/// <summary>
/// Names the abstract pointer buttons exposed by a pointer device.
/// </summary>
public enum InputPointerButton {
    /// <summary>
    /// The primary pointer button.
    /// </summary>
    Primary = 0,
    /// <summary>
    /// The secondary pointer button.
    /// </summary>
    Secondary = 1,
    /// <summary>
    /// The middle pointer button.
    /// </summary>
    Middle = 2,
    /// <summary>
    /// The back pointer button.
    /// </summary>
    Back = 3,
    /// <summary>
    /// The forward pointer button.
    /// </summary>
    Forward = 4
}
